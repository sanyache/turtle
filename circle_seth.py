import turtle as t

# turtle.circle(radius, extent=None, steps=None)
t.title('circle')
t.setup(600, 600)
t.color('brown', 'orange')
t.width(1)
# малюємо вісь x
t.setposition(-300, 0)
t.forward(600)
t.up()
t.goto(0, -300)
t.down()
t.left(90)
t.forward(600)
# повертаємось в точку (0, 0)
t.home()
t.setheading(0)
t.circle(100)
t.setheading(90)
t.circle(100)
t.setheading(180)
t.circle(100)
t.setheading(270)
t.circle(100)
t.mainloop()